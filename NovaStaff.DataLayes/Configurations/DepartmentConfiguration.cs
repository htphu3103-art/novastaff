using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata;
using Microsoft.EntityFrameworkCore.Metadata.Builders;
using NovaStaff.Models.Entities;

namespace NovaStaff.DataLayers.Configurations;

public class DepartmentConfiguration : IEntityTypeConfiguration<Department>
{
    public void Configure(EntityTypeBuilder<Department> builder)
    {
        // ?? Base Audit Fields (CreatedDate, ModifiedDate, etc.) ?????????????????
        BaseEntityConfiguration.ConfigureBaseEntity(builder);

        // ?? B?ng & Constraints ?????????????????????????????????????????????????
        builder.ToTable("Departments", t =>
        {
            t.HasCheckConstraint("CK_Department_Name", "LEN(LTRIM(DepartmentName)) > 0");
            t.HasCheckConstraint("CK_Department_Code", "Code IS NULL OR LEN(LTRIM(Code)) > 0");
            
            // Thêm Temporal Table n?u b?n mu?n lýu l?ch s? thay ŵ?i (tùy ch?n cho 10/10)
            // t.IsTemporal(); 
        });

        // ?? Primary Key ????????????????????????????????????????????????????????
        builder.HasKey(d => d.DepartmentID);
        builder.Property(d => d.DepartmentID)
               .UseIdentityColumn();

        // ?? Properties ?????????????????????????????????????????????????????????
        builder.Property(d => d.DepartmentName)
            .IsRequired()
            .HasMaxLength(100)
            .HasColumnType("nvarchar(100)")
            .HasComment("Tên ph?ng ban/b? ph?n");

        builder.Property(d => d.Code)
            .HasMaxLength(20)
            .IsUnicode(false)
            .HasColumnType("varchar(20)")
            .HasComment("M? ŵ?nh danh ph?ng ban");

        builder.Property(d => d.OrgNode)
            .IsRequired()
            .HasColumnType("hierarchyid");

        builder.Property(d => d.OrgLevel)
            .HasColumnType("smallint")
            .HasComputedColumnSql("[OrgNode].GetLevel()", stored: false);

        builder.Property(d => d.OrgLevel)
            .Metadata.SetAfterSaveBehavior(PropertySaveBehavior.Ignore);

        // Ŵ? ŵ?i tên thành ManagerEmployeeID ŵ? ŵ?ng b? v?i b?ng Employee
        builder.Property(d => d.ManagerEmployeeID) 
            .HasColumnType("int")
            .HasComment("ID nhân viên ŵang gi? ch?c v? trý?ng ph?ng");

        builder.Property(d => d.IsActive)
            .IsRequired()
            .HasDefaultValue(true);

        builder.Property(d => d.Description)
            .HasMaxLength(500)
            .HasColumnType("nvarchar(500)");

        // ?? Relationships ??????????????????????????????????????????????????????

        // 1. M?t ph?ng ban có nhi?u nhân viên
        builder.HasMany(d => d.Employees)
            .WithOne(e => e.Department)
            .HasForeignKey(e => e.DepartmentID)
            .OnDelete(DeleteBehavior.Restrict);

        // 2. M?t ph?ng ban có M?T trý?ng ph?ng (tr? v? b?ng Employee)
        builder.HasOne(d => d.Manager)
            .WithMany() // Không c?n t?o ICollection<Department> bên Employee cho ch?c danh này
            .HasForeignKey(d => d.ManagerEmployeeID)
            .OnDelete(DeleteBehavior.Restrict);

        // ?? Global Query Filter ????????????????????????????????????????????????
        

        // ?? Indexes ????????????????????????????????????????????????????????????
        builder.HasIndex(d => d.OrgNode)
            .IsUnique()
            .HasDatabaseName("IX_Departments_OrgNode");

        builder.HasIndex(d => d.Code)
            .IsUnique()
            .HasDatabaseName("IX_Departments_Code");

        // Index h? tr? t?m ki?m theo Trý?ng ph?ng nhanh hõn
        builder.HasIndex(d => d.ManagerEmployeeID)
            .HasDatabaseName("IX_Departments_ManagerEmployeeID");

        
    }
}



